//! HKDF (HMAC-based Key Derivation Function) per RFC 5869.
//!
//! Wraps std.crypto.hkdf for use in RFC 9580 native key types.
//! HKDF is used for key derivation in X25519 and X448 key agreement.

const std = @import("std");

/// HKDF-SHA256 key derivation.
///
/// This is a thin wrapper around the Zig standard library's HKDF implementation,
/// providing a slightly simplified interface for OpenPGP usage.
pub const HkdfSha256 = struct {
    const Inner = std.crypto.kdf.hkdf.HkdfSha256;

    /// Extract a pseudorandom key (PRK) from salt and input keying material.
    ///
    /// PRK = HMAC-SHA256(salt, ikm)
    pub fn extract(salt: []const u8, ikm: []const u8) [32]u8 {
        return Inner.extract(salt, ikm);
    }

    /// Expand a PRK into output keying material using an info string.
    ///
    /// OKM = T(1) || T(2) || ... truncated to out.len
    /// where T(i) = HMAC-SHA256(PRK, T(i-1) || info || i)
    pub fn expand(out: []u8, info: []const u8, prk: [32]u8) void {
        Inner.expand(out, info, prk);
    }

    /// One-shot: extract-then-expand.
    ///
    /// Derives `out.len` bytes of key material from salt, ikm, and info.
    pub fn deriveKey(out: []u8, salt: []const u8, ikm: []const u8, info: []const u8) void {
        const prk = extract(salt, ikm);
        expand(out, info, prk);
    }
};

/// HKDF-SHA512 key derivation.
pub const HkdfSha512 = struct {
    const Inner = std.crypto.kdf.hkdf.HkdfSha512;

    pub fn extract(salt: []const u8, ikm: []const u8) [64]u8 {
        return Inner.extract(salt, ikm);
    }

    pub fn expand(out: []u8, info: []const u8, prk: [64]u8) void {
        Inner.expand(out, info, prk);
    }

    pub fn deriveKey(out: []u8, salt: []const u8, ikm: []const u8, info: []const u8) void {
        const prk = extract(salt, ikm);
        expand(out, info, prk);
    }
};

